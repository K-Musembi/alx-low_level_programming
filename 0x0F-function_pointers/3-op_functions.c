#include "3-calc.h"

/**
* op_add - add
* @a: input
* @b: input
*
* Return: sum
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
* op_sub - subtract
* @a: input
* @b: input
*
* Return: difference
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_mul - multiply
* @a: input
* @b: input
*
* Return: product
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
* op_div - divide
* @a: input
* @b: input
*
* Return: division
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
* op_mod - modulus
* @a: input
* @b: input
*
* Return: remainder
*/
int op_mod(int a, int b)
{
	return (a % b);
}
